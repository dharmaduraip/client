			return redirect('dashboard')->with('message', __('core.note_restric'))->with('status','error');	
			$this->data['total_wallet'] = $this->data['hand_in_cash'] = 0.00;
			if(!is_null($request->input('search')))	{
				$u_id = explode(':', $request->input('search'))[2];
				if ($u_id) {
					$user	= User::find($u_id);
				$this->data['total_wallet'] = $user->customer_wallet ;
				$this->data['hand_in_cash'] = $user->boy_cashIn_hand ;
				}
			}		
		return view( $this->module.'.index',$this->data);
	}	

